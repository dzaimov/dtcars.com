package com.dtcars.exceptions;

public class AdException extends Exception {

	private static final long serialVersionUID = 6251870172098804057L;

	public AdException() {
		super();
	}

	public AdException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public AdException(String arg0) {
		super(arg0);
	}

	public AdException(Throwable arg0) {
		super(arg0);
	}

}
